# Exibindo números de 2 em 2 até 10 usando um laço for
print("Números de 2 em 2 até 10 (usando laço for):")
for i in range(2, 11, 2):
    print(i)

print("\n" + "-" * 30 + "\n")

# Exibindo números de 2 em 2 até 10 usando um laço while
print("Números de 2 em 2 até 10 (usando laço while):")
contador = 2
while contador <= 10:
    print(contador)
    contador += 2

