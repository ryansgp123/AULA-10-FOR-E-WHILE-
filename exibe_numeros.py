# Exibindo números de 1 a 10 usando um laço for
print("Números de 1 a 10 (usando laço for):")
for i in range(1, 11):
    print(i)

print("\n" + "-" * 30 + "\n")

# Exibindo números de 1 a 10 usando um laço while
print("Números de 1 a 10 (usando laço while):")
contador = 1
while contador <= 10:
    print(contador)
    contador += 1

