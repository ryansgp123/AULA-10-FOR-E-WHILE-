# Demonstração de 5 repetições usando um laço for
print("--- Repetições usando laço FOR ---")
for i in range(5):
    print(f"Repetição FOR número {i + 1}")

print("\n" + "-" * 30 + "\n")

# Demonstração de 5 repetições usando um laço while
print("--- Repetições usando laço WHILE ---")
contador = 0
while contador < 5:
    print(f"Repetição WHILE número {contador + 1}")
    contador += 1

